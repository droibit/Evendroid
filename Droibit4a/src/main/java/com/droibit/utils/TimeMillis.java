package com.droibit.utils;

/**
 * 1秒、分、時間のミリ単位(通常はlong)
 * 
 * @author kumagai
 *
 */
public final class TimeMillis {

	/** 1秒(1000m) */
	public static final long SECOND = 1000L;

	/** 1分(60000m) */
	public static final long MINUTE = 60L * SECOND;

	/** 1時間(3600000m) */
	public static final long HOUR = 60L * MINUTE;
}
